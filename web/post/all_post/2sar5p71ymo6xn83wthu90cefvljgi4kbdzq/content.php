Dựa trên nghiên cứu có từ năm 1938 và khai thác quá trình xử lý thông tin cơ học, các nhà khoa học đã xây dựng một mạch tích hợp vào một dạng vật liệu tiên tiến. Đây là vật liệu polyme mềm, hoạt động giống như một bộ não, theo tạp chí khoa học Nature.
<br><br>
Ông Ryan Harne, phó giáo sư kỹ thuật cơ khí tại Đại học bang Pennsylvania, cho biết: "Chúng tôi đã tạo ra thử nghiệm đầu tiên về một vật liệu kỹ thuật có thể đồng thời cảm nhận, suy nghĩ và hành động khi bị căng thẳng cơ học mà không cần thêm các mạch bổ sung để xử lý các tín hiệu đó".
<br><br>
Theo ông Harne, vật liệu polyme mềm hoạt động giống như một bộ não. Nó nhận được các chuỗi thông tin kỹ thuật số, sau đó xử lý, tạo ra các chuỗi thông tin kỹ thuật số mới để điều khiển các phản ứng.
<br><br>
Nhóm nghiên cứu phát hiện rằng "gần như bất kỳ vật liệu nào" có sẵn của loài người đều có khả năng được sử dụng để tạo ra các mạch tích hợp có khả năng thực hiện các hoạt động tính toán.
<br><br>
Công nghệ này được so sánh với não người, dựa trên các mạch tích hợp thường dựa vào chất bán dẫn silicon để xử lý thông tin.
<br><br>
Các mạch sẽ được cấu hình lại nhằm sử dụng trong hoạt động của vật liệu, để các kích thích bên ngoài được thu nhận và chuyển đổi thành thông tin điện, sau đó sẽ đưa đi xử lý để tạo tín hiệu đầu ra.
<br><br>
Vật liệu này cũng được sử dụng để phát hiện các tần số vô tuyến, giúp gửi tín hiệu ánh sáng cho các ứng dụng như hệ thống tìm kiếm và cứu nạn. Các nhà khoa học đã cho thấy khả năng của vật liệu bằng cách cho nó thực hiện các phép tính tinh vi.
<br><br>
Thậm chí vật liệu này cũng sẽ được đưa vào các vật liệu lai sinh học có khả năng xác định vị trí, phân tách và loại bỏ nhiễm trùng trong không khí.
<br><br>
Giờ đây, các nhà nghiên cứu tiếp tục cải thiện chất liệu này để nó có thể giải thích dữ liệu hình ảnh tương tự như cách nó "cảm nhận" các tín hiệu vật lý.
