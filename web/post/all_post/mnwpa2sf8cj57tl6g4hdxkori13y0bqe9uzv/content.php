Giới quan sát nhận định Apple đã tránh tăng giá những chiếc iPhone bán chạy nhất của mình trong lần ra mắt sản phẩm lớn nhất trong năm, tập trung vào nâng cấp an toàn hơn là thông số kỹ thuật.
<br><br>
Theo Hãng tin Reuters, nhà sản xuất iPhone tập trung vào các công nghệ an toàn để tăng thêm sức hấp dẫn cho các thiết bị của mình, ví dụ như khả năng phát hiện tai nạn xe hay hỗ trợ người dùng cầu cứu từ đỉnh núi hẻo lánh.
<br><br>
Ben Bajarin, người đứng đầu bộ phận công nghệ tiêu dùng tại Creative Strategies, nhận định những tính năng này khiến người dùng "không chỉ muốn sở hữu sản phẩm cho mình mà còn cho những người thân yêu".
<br><br>
Điện thoại thông minh iPhone vốn đóng góp một nửa doanh số của Apple. Các mẫu iPhone 14 có những tinh chỉnh về camera và thời lượng pin, nhưng chỉ có dòng iPhone Pro được nâng cấp lên chip xử lý hoàn toàn mới.
<br><br>
Giá của iPhone 14 tương đương với các mẫu iPhone 13 của năm ngoái.
<br><br>
Apple đã bỏ đi sản phẩm rẻ nhất của mình trong các mẫu mới, iPhone Mini. Điều này có nghĩa mẫu có giá thấp nhất của hãng hiện sẽ đắt hơn 100 USD so với năm ngoái.
<br><br>
iPhone 14 có giá khởi điểm là 799 USD và iPhone 14 Plus là 899 USD. Người mua có thể đặt hàng trước bắt đầu từ ngày 9-9.
<br><br>
Trong khi đó, iPhone Pro sẽ có giá 999 USD và iPhone Pro Max là 1.099 USD. Hai mẫu này sẽ được tung ra vào ngày 16-9.
<br><br>
Theo Apple, tính năng SOS vệ tinh của iPhone sẽ hoạt động phối hợp với các nhóm cứu hộ. Người dùng sẽ có thể sử dụng ứng dụng FindMy để chia sẻ vị trí qua vệ tinh khi họ không có kết nối khác.
<br><br>
Apple cho biết dịch vụ này sẽ miễn phí trong vòng 2 năm với iPhone 14, nhưng không đề cập điều gì sẽ xảy ra sau khoảng thời gian đó.
<br><br>
Apple cũng đã giới thiệu bộ ba đồng hồ Apple Watch mới, bao gồm một mẫu Ultra Watch mới dành cho các môn thể thao mạo hiểm và lặn.
<br><br>
Đồng hồ Ultra Watch giá 799 USD, có pin lớn hơn để duy trì hoạt động qua các sự kiện thể thao kéo dài. Đồng hồ này còn có khả năng chống thấm nước và chịu nhiệt tốt hơn để hoạt động trong môi trường ngoài trời, cũng như kết nối GPS tốt hơn cho thể thao.
<br><br>
Bên cạnh đó, đồng hồ Series 8 có giá tương đương năm ngoái và mẫu SE được cập nhật sẽ có giá rẻ hơn.
<br><br>
Tất cả các mẫu đồng hồ và iPhone mới sẽ có khả năng phát hiện khi người dùng gặp tai nạn xe hơi nghiêm trọng và gọi dịch vụ khẩn cấp.
<br><br>
Đồng hồ Series 8 mới còn có cảm biến nhiệt độ, giúp phát hiện ngày rụng trứng ở phụ nữ. Apple nhấn mạnh hãng chọn cách tiếp cận bảo mật để theo dõi chu kỳ kinh nguyệt của người dùng.
<br><br>
Hãng cũng quảng cáo rằng mẫu tai nghe AirPods Pro thế hệ thứ hai của họ sẽ tăng gấp đôi khả năng khử tiếng ồn so với phiên bản gốc.
