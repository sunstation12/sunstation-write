Cụ thể, một vài chiếc máy tính cũ chạy Windows XP sẽ gặp hư hỏng nghiêm trọng liên quan đến ổ cứng nếu người dùng bật MV ca nhạc của “Rhythm Nation” của nữ minh tinh Janet Jackson.
