<p>Origami, hay còn gọi là xếp giấy, có thể coi là một trong những bộ môn nghệ thuật nổi tiếng của đất nước Nhật Bản. Tuy nhiên, lại không nhiều người biết đến kirigami, một môn nghệ thuật từ cắt giấy đa chiều và biến hóa đến kinh ngạc của xứ sở hoa anh đào. Và mới đây, nhà thiết kế Haruki Nakamura đã cho chúng ta thấy được sự ảo diệu trong các tác phẩm cắt giấy tạo hình kirigami.
<br><br>
Nếu như origami truyền thống không cho phép người chơi được dùng gì ngoài giấy thì kirigami lại hoàn toàn khác biệt. Người chơi kirigami có thể thoải mái sử dụng kéo cắt, hồ dán để tạo hình, cũng chính vì thế mà tính sáng tạo của kirigami cao hơn hẳn. Nhờ vào điều này, nhà thiết kế Nakamura đã có thể tạo ra các tác phẩm gấp giấy biến đổi hình dạng độc đáo có một không hai.</p>
